    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
//        Category category = new Category();
//        category.setName(txtName.getText());
//        CategoryDAO.save(category);
//        setVisible(false);
//        new ManageCategory().setVisible(true);

        try {
            Category category = new Category();
            category.setName(txtName.getText());
            CategoryDAO.save(category);

            // Refresh the table with updated data
            DefaultTableModel dtm = (DefaultTableModel) categoryTable.getModel();
            dtm.setRowCount(0); // Clear the existing rows
            ArrayList<Category> list = CategoryDAO.getAllRecords();
            for (Category categoryObj : list) {
                dtm.addRow(new Object[]{categoryObj.getId(), categoryObj.getName()});
            }

            // Clear the text field after saving
            txtName.setText("");
        } catch (Exception ex) {
            // Handle the exception, e.g., show an error message dialog
            JOptionPane.showMessageDialog(this, "Error occurred: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace(); // Print the stack trace for debugging purposes
        }
    }//GEN-LAST:event_btnSaveActionPerformed

