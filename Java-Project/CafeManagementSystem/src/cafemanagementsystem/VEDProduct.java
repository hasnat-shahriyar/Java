    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
   // Clear the jTable
    DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
    dtm.setRowCount(0); // Clears existing rows

    // Initialize IDLabel
    IdLabel.setText("00");

    // Add new rows
    ArrayList<Product> list = ProductDAO.getAllRecords();
    Iterator<Product> itr = list.iterator();
    while (itr.hasNext()) {
        Product productObj = itr.next();
        dtm.addRow(new Object[]{productObj.getId(), productObj.getName(), productObj.getCategory(), productObj.getPrice()});
    }
    }//GEN-LAST:event_formComponentShown

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
    // Check if a row is selected
    int index = jTable1.getSelectedRow();
    if (index == -1) {
        // No row is selected, handle this case accordingly
        return;
    }

    TableModel model = jTable1.getModel();
    if (model == null) {
        // Handle the case where the model is null
        return;
    }

    try {
        String id = model.getValueAt(index, 0).toString();
        IdLabel.setText(id);

        String name = model.getValueAt(index, 1).toString();
        txtName.setText(name);

        String category = model.getValueAt(index, 2).toString();
        String price = model.getValueAt(index, 3).toString();
        txtPrice.setText(price);

        // Enable update and delete buttons
        btnUpdate.setEnabled(true);
        btnDelete.setEnabled(true);

        // Clear and populate the category combo box
        jComboBox1.removeAllItems();
        jComboBox1.addItem(category);

        // Retrieve all categories and populate the combo box
        ArrayList<Category> categoryList = CategoryDAO.getAllRecords();
        if (categoryList != null) {
            Iterator<Category> categoryItr = categoryList.iterator();
            while (categoryItr.hasNext()) {
                Category categoryObj = categoryItr.next();
                if (!categoryObj.getName().equals(category)) {
                    jComboBox1.addItem(categoryObj.getName());
                }
            }
        } else {
            // Handle the case where categoryList is null
        }
    } catch (NullPointerException | ArrayIndexOutOfBoundsException | NumberFormatException ex) {
        // Handle exceptions gracefully
        ex.printStackTrace(); // Or log the exception
        // Optionally, show a message to the user indicating an error occurred
    }
    }//GEN-LAST:event_jTable1MouseClicked

